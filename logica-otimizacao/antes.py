month = input("Digite a sigla de um mês para ter o nome completo dele: ")
if month.lower() == "jan":
    print("Janeiro")
elif month.lower() == "fev":
    print("Fevereiro")
elif month.lower() == "mar":
    print("Março")
elif month.lower() == "abr":
    print("Abril")
elif month.lower() == "mai":
    print("Maio")
elif month.lower() == "jun":
    print("Junho")
elif month.lower() == "jul":
    print("Julho")
elif month.lower() == "ago":
    print("Agosto")
elif month.lower() == "set":
    print("Setembro")
elif month.lower() == "out":
    print("Outubro")
elif month.lower() == "nov":
    print("Novembro")
elif month.lower() == "dez":
    print("Dezembro")
else:
    print("Sigla inválida!")
